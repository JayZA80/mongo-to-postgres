class Video {
    constructor(id, title, type, topic_id, published_at, available_at, duration, status, songcount, channel, maxccv) {
        this.id = id;
        this.title = title;
        this.type = type;
        this.topic_id = topic_id;
        this.published_at = published_at;
        this.available_at = available_at;
        this.duration = duration;
        this.status = status;
        this.songcount = songcount;
        this.channel = channel;
        this.maxccv = maxccv;
    }
}